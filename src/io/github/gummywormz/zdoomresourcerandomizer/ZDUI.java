    private void randomizeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_randomizeActionPerformed
        
        ArrayList<String> args = new ArrayList<>();
        
        
        int returnVal = dirChooser.showOpenDialog(ZDUI.this);
        if (returnVal == JFileChooser.APPROVE_OPTION) 
        {
            args.add("-f");
            args.add(dirChooser.getSelectedFile().getAbsolutePath());
        }else{return;}
        
        for(String s : tFiles)
        {
            args.add("-t");
            args.add(s);
        }
        
        for(String s : aFiles)
        {
            args.add("-a");
            args.add(s);
        }
        
        for(String s : lFiles)
        {
            args.add("-l");
            args.add(s);
        }
        
        for(String s : sFiles)
        {
            args.add("-s");
            args.add(s);
        }
        
        for(String s : spFiles)
        {
            args.add("-p");
            args.add(s);
        }
        
        if(!langString.getText().isEmpty()){
        args.add("--LANGUAGEHEADER");
        args.add(langString.getText());
        }
        
        if(!randTics.getText().isEmpty() && !randTics.getText().equals("0")){
        args.add("--RANDOMIZETICS");
        args.add(randTics.getText());
        }
        
        if(sndOnce.isSelected())
        {
            args.add("--USESOUNDONCE");
        }
        
        if(msgOnce.isSelected())
        {
            args.add("--USEMSGONCE");
        }
        
        outputArea.setText("");
        
        statusWindow.show();
        
        PrintStream p = new PrintStream(new MsgOutputStream(outputArea));
        
        System.setOut(p);
        System.setErr(p);
        
        String[] args2 = args.toArray(new String[0]);
        
        Main.build(args2);
    }//GEN-LAST:event_randomizeActionPerformed

